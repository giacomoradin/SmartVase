/*!
    @enum  CppMovementState
    @brief State of the movement state machine, C++ side.

    @details Kept as a separate type from the `MovementState` protobuf enum to
             not tie the internal logic of main to the network schema; the
             conversion to the protobuf type happens explicitly with
             ::cppMovementStateToProto.
*/
typedef enum {
    CPP_M_IDLE,            /**< Robot stopped, no active target movement. */
    CPP_M_MOVING,           /**< Normal movement (light/shadow seeking or no search). */
    CPP_M_AVOID_START,      /**< Entry into the obstacle avoidance sequence. */
    CPP_M_AVOID_REVERSING,  /**< Reverse phase of obstacle avoidance. */
    CPP_M_AVOID_TURNING,    /**< Rotation phase of obstacle avoidance. */
    CPP_M_STUCK,            /**< Robot stuck, waiting for backoff before a new attempt. */
    CPP_M_SCAN_ROTATE,      /**< Light scan: rotation in place sampling the LDR by sectors (see startLightScan). */
    CPP_M_SCAN_ALIGN        /**< Light scan: second rotation to the best identified sector. */
} CppMovementState;

/*!
    @enum  CppMode
    @brief Robot operating modes (set via CLI or Hub `setMode` command).
*/
typedef enum {
    CPP_IDLE,    /**< No active search: the robot stays still if not in manual movement. */
    CPP_LIGHT,   /**< Search for light (light seeking). */
    CPP_SHADOW   /**< Search for shadow (shadow seeking). */
} CppMode;

/*!
    @brief    Converts a C++ movement state into the corresponding protobuf enum.

    @param[in] s Movement state on the C++ side.

    @return   The equivalent ::MovementState value to send in `TelemetryFast`.

    @note     `CPP_M_IDLE` and any unrecognized value map to `MS_IDLE` (fail-safe default).
*/
static inline MovementState cppMovementStateToProto(CppMovementState s) {
    switch (s) {
        case CPP_M_MOVING:          return MS_MOVING;
        // Internal light scan states are a form of active movement:
        // towards the network schema (v4.0, no scan states) they are reported as MOVING.
        case CPP_M_SCAN_ROTATE:     return MS_MOVING;
        case CPP_M_SCAN_ALIGN:      return MS_MOVING;
        case CPP_M_AVOID_START:     return MS_AVOID_START;
        case CPP_M_AVOID_REVERSING: return MS_AVOID_REVERSING;
        case CPP_M_AVOID_TURNING:   return MS_AVOID_TURNING;
        case CPP_M_STUCK:           return MS_STUCK;
        case CPP_M_IDLE:
        default:                    return MS_IDLE;
    }
}

