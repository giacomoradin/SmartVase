    uint32_t total_irrigation_duration_s;    /**< Cumulative duration of irrigations, in seconds. */
    uint32_t light_seeking_sessions;         /**< Number of light seeking sessions (LIGHT mode). */
    uint32_t shadow_seeking_sessions;        /**< Number of shadow seeking sessions (SHADOW mode). */
    uint32_t obstacles_avoided;              /**< Number of successfully avoided obstacles. */
    uint32_t escape_attempts;                /**< Number of anti-circling relocation attempts during seeking. */
    uint32_t stuck_events;                   /**< Number of times the robot entered the M_STUCK state. */
